#include "crypt.h"

void encrypt(char *msg)
{
	while(*msg)//სანამ მასივის ელემენტები არ ამოიწურება, ბოლო ელემენტი არის 0 (ანუ სანამ მასივის ელემენტის მნიშვნელობა არ გახდება 0)
	{
		*msg = *msg + 1; //ჩარების მასივის პირველი ელემენტის მნიშვნელობა გაზარდა 1 ით
		msg++; // ჩარების მასივის მისამართი გაზარდა ანუ აირჩია მასივის მომდევნო ელემენტი 
	}

}

void decrypt(char *msg)
{
	while(*msg)
	{
		*msg = *msg - 1;
		msg++;
	}

}
